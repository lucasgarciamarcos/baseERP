    public void atualizar() {   //Metodo de Atualização da View
        DefaultTableModel val = (DefaultTableModel) jTable1.getModel();

        val.setRowCount(0);

        try {
            Vector view = estoque.listarProdutos();

            for (int i = 0; i < view.size(); i++) {

                Vector vec = new Vector();

                vec.addElement(String.valueOf(((Produto) (view.elementAt(i))).getCod()));
                vec.addElement(((Produto) (view.elementAt(i))).getNome());
                vec.addElement(((Produto) (view.elementAt(i))).getQuantidade());
                vec.addElement(((Produto) (view.elementAt(i))).getPreco());

                val.addRow(vec);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

