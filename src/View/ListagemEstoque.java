    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Produto produto = new Produto();

        DefaultTableModel val = (DefaultTableModel) jTable1.getModel();
        int cod = Integer.parseInt((String) val.getValueAt(jTable1.getSelectedRow(), 0));
        produto.setCod(cod);

        try {
            if (estoque.removerProduto(produto)) {
                JOptionPane.showMessageDialog(null, "Produto Deletado");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao deletar o produto");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ListagemEstoque.class.getName()).log(Level.SEVERE, null, ex);
        }
        atualizar();

    }//GEN-LAST:event_jButton2ActionPerformed

