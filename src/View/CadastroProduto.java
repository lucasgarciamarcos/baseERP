    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Produto produto = new Produto();    // Método de Adição de Produto
        produto.setQuantidade(Integer.parseInt(jTextField3.getText()));
        produto.setNome(jTextField2.getText());
        produto.setPreco(Float.parseFloat(jTextField4.getText()));

        try {
            if (estoque.adicionarProduto(produto)){
                JOptionPane.showMessageDialog(null, "Produto adicionado");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao adicionar Produto");
            }
        } catch (SQLException ex) {
            Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        }

        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Produto produto = new Produto();    //Alteração de Produto
        produto.setNome(jTextField2.getText());
        produto.setQuantidade(Integer.parseInt(jTextField3.getText()));
        produto.setPreco(Float.parseFloat(jTextField4.getText()));
        produto.setCod(Integer.parseInt(jTextField1.getText()));

        try {
            if (estoque.alterarProduto(produto)){
                JOptionPane.showMessageDialog(null, "Produto alterado");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao alterar Produto");
            }
        } catch (SQLException ex) {
            Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
    }//GEN-LAST:event_jButton4ActionPerformed

